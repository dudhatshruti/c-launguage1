//area of triangle //

#include<stdio.h>
int main()
{
    float base,height,area;
    printf("base:");
    scanf("%f",&base);

    printf("height:");
    scanf("%f",&height);

    area=0.5*base*height;
    printf("area of triangle:%f",area);
}

/*output :-

base:89
height:20
area of triangle:890.000000

*/