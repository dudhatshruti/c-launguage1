// 6.verify the formula L=(c+d)*(g+h) //

#include<stdio.h>
int main()
{
    int c,d,g,h,L;
    printf("c:");
    scanf("%d",&c);

    printf("d:");
    scanf("%d",&d);

    printf("g:");
    scanf("%d",&g);

    printf("h:");
    scanf("%d",&h);

    L=(c+d)*(g+h);
    printf("L=%d",L);

}

